      <h2 class="category-title">Logika</h2>
          <p>
            Bertrand Russell był współtwórcą i animatorem, wspólnie z Alfredem N. Whiteheadem, programu 
            logicyzacji matematyki. W dziele Principia Mathematica podał sposób redukcji aksjomatów arytmetyki 
            liczb naturalnych do języka logiki. Koncepcja ta została zrealizowana dzięki stworzeniu teorii typów 
            co pozwalało uniknąć antynomii zbioru wszystkich zbiorów oraz klas samozwrotnych, 
            będących wadą poprzednich prób tego typu (Frege). 
          </p>
          <p>
            Ceną była nierealistyczna komplikacja. Dlatego teoria typów nigdy się w matematyce nie przyjęła, 
            choć cieszyła się sentymentem nielicznych logików. Teoria ta dała natomiast początek typom powszechnie
            używanym w dzisiejszych językach programowania. 
          </p>
          <p>
            Teoria typów unika paradoksów poprzez wprowadzenie i rygorystyczne separowanie różnych typów. 
            Wymaga to definiowania tego samego obiektu matematycznego wielokrotnie, na różnych poziomach typów. 
            W szczególności nie ma jednego zbioru liczb naturalnych, lecz potencjalnie nieskończenie wiele. 
            Wprowadza się je za każdym razem, gdy ma się do czynienia z następnym poziomem i rodzajem typu. 
            Logikom to nie przeszkadza, ale matematyk tak pracować praktycznie nie może. 
          </p>
