           poszczególnych jednostek, lecz dotykają całe społeczeństwa (tworzy się tzw. gorączka wojenna). 
           Zbiorowy impuls nie bierze pod uwagę racjonalnych czynników i przeciwwskazań co do prowadzenia wojny 
           – impuls jest całkowicie irracjonalny. Nie odróżniał wojen napastniczych i obronnych 
           – były one dla niego jednakowo groźne i nieakceptowalne. Interesowały go natomiast skutki 
           – sposoby usprawiedliwiania działań wojennych. 
